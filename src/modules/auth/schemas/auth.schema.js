import { z } from "zod";

// ============================================================
// auth.schema.js — قواعد التحقق (Validation) باستخدام Zod
// ============================================================
// Zod مكتبة تتحقق من البيانات وترجع أخطاء واضحة.
// كل schema هو "عقد" يحدد شكل البيانات المطلوبة.
// ============================================================

// ------------------------------------------------------------
// schema تسجيل الدخول
// ------------------------------------------------------------
export const loginSchema = z.object({
  // البريد الإلكتروني: يجب أن يكون صيغة email صحيحة
  email: z.string().min(1, "البريد الإلكتروني مطلوب").email("صيغة البريد الإلكتروني غير صحيحة"),

  // كلمة المرور: 8 أحرف على الأقل
  password: z
    .string()
    .min(1, "كلمة المرور مطلوبة")
    .min(8, "كلمة المرور يجب أن تكون 8 أحرف على الأقل"),
});

// ------------------------------------------------------------
// schema استعادة كلمة المرور
// ------------------------------------------------------------
export const forgotPasswordSchema = z.object({
  email: z.string().min(1, "البريد الإلكتروني مطلوب").email("صيغة البريد الإلكتروني غير صحيحة"),
});

// ------------------------------------------------------------
// schema إنشاء الحساب
// ------------------------------------------------------------
export const registerSchema = z
  .object({
    // الاسم الكامل: 2 أحرف على الأقل
    fullName: z.string().min(1, "الاسم الكامل مطلوب").min(2, "الاسم يجب أن يكون حرفين على الأقل"),

    email: z.string().min(1, "البريد الإلكتروني مطلوب").email("صيغة البريد الإلكتروني غير صحيحة"),

    password: z
      .string()
      .min(1, "كلمة المرور مطلوبة")
      .min(8, "كلمة المرور يجب أن تكون 8 أحرف على الأقل")
      .regex(/[A-Z]/, "يجب أن تحتوي على حرف كبير واحد على الأقل")
      .regex(/[0-9]/, "يجب أن تحتوي على رقم واحد على الأقل"),

    confirmPassword: z.string().min(1, "تأكيد كلمة المرور مطلوب"),
  })
  // .refine() يتحقق من علاقة بين حقلين مختلفين
  .refine((data) => data.password === data.confirmPassword, {
    message: "كلمتا المرور غير متطابقتين",
    path: ["confirmPassword"], // الخطأ يظهر تحت حقل confirmPassword
  });
